      fromShipmentLocation,
      fromShipmentOtherLocation,
      fromCountryId,
      fromCity,
      fromCityLat,
      fromCityLng,
      toShipmentLocation,
      toShipmentOtherLocation,
      toCountryId,
      toCity,
      toCityLat,
      toCityLng,
      total,
      currencyId,
      shipmentReady,
      content,
      insurance,
      customsClearance,
      certificates,
      shipmentSizes;

  final String? through;

  final List<String> certificate,
      containerType,
      containerCount,
      containerContent,
      goodsTotalWeight,
      goodsOverallSize,
      goodsUnitType,
      goodsLength,
      goodsHeight,
      goodsWidth,
      goodsWeightPerUnit,
      goodsCM,
      goodsQuantity,
