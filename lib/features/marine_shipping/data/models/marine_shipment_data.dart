    required this.currencyId,
    required this.shipmentReady,
    required this.content,
    required this.insurance,
    required this.customsClearance,
    required this.certificates,
    required this.shipmentSizes,
    required this.through,
    required this.certificate,
    required this.containerType,
    required this.containerCount,
    required this.containerContent,
    required this.goodsTotalWeight,
    required this.goodsOverallSize,
    required this.goodsUnitType,
    required this.goodsLength,
    required this.goodsHeight,
    required this.goodsWidth,
    required this.goodsWeightPerUnit,
    required this.goodsCM,
    required this.goodsQuantity,
    required this.image,
